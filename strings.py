#concatenate
nome = 'Marcos da Silva'
print(nome + ' tem 19 anos de idade')

#FUNCAO PARA ALTERAR PARA MAIUSCULO
nome = 'Marcos da Silva'
print(nome.upper())

#FUNCAO PARA ALTERAR PARA MINUSCULO
nome = 'Marcos da Silva'
print(nome.lower())

#FUNCAO PARA CONTAR OS CARACTERES
nome = 'Marcos da Silva'
print(len(nome))

#FUNCAO PARA RETORNAR A LETRA (NAO ESQUECER QUE NO PY SE INICIA NO 0)
nome = 'Marcos da Silva'
